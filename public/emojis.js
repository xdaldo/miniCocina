// Función para obtener emoji, si no existe devuelve "🔧"
function getIngredientIcon(nombre) {
  const nombreLower = nombre.toLowerCase().trim();
  if (diccionarioEmojis[nombreLower]) return diccionarioEmojis[nombreLower];
  for (let [key, emoji] of Object.entries(diccionarioEmojis)) {
    if (nombreLower.includes(key)) return emoji;
  }
  return "🔧"; // icono de tuerca = pendiente
}